/*
CALLBACKS.-Se usan callbacks cuando se quiere ejecutar una funcion despues de que otra tarea termine 
especialmente en operaciones simples o cortas.El problema es que cuando hay muchas operaciones encadenadas
el codigo se vuelve dificil de leer, lo que es conocido como "callback hell";

PROMESAS.-se usan cuando se trabaja con operaciones asincronas mas complejas (a diferencia del callback)
o procesos que se pueden tardar, las promesas permiten encadenar operaciones con .then() y manejar errores
con .catch(), esto permite que el codigo sea mas origanizado y facil de enender.
*/